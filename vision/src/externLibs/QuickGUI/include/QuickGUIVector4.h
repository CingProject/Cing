#ifndef QUICKGUIVECTOR4_H
#define QUICKGUIVECTOR4_H

#include "QuickGUIExportDLL.h"

namespace QuickGUI
{
	/*
	* Simple container of 4 float values.
	*/
	class _QuickGUIExport Vector4
	{
	public:
		float x,y,z,w;

		// special
        static const Vector4 ZERO;

		inline Vector4()
        {
        }

		inline Vector4( const float fX, const float fY, const float fZ, const float fW )
            : x( fX ), y( fY ), z( fZ ), w( fW)
        {
        }

		/** Assigns the value of the other vector.
            @param
                rkVector The other vector
        */
        inline Vector4& operator = ( const Vector4& rkVector )
        {
            x = rkVector.x;
            y = rkVector.y;
            z = rkVector.z;
            w = rkVector.w;

            return *this;
        }

		inline Vector4& operator = ( const float fScalar)
		{
			x = fScalar;
			y = fScalar;
			z = fScalar;
			w = fScalar;
			return *this;
		}

        inline bool operator == ( const Vector4& rkVector ) const
        {
            return ( x == rkVector.x &&
                y == rkVector.y &&
                z == rkVector.z &&
                w == rkVector.w );
        }

        inline bool operator != ( const Vector4& rkVector ) const
        {
            return ( x != rkVector.x ||
                y != rkVector.y ||
                z != rkVector.z ||
                w != rkVector.w );
        }
	};
}

#endif
